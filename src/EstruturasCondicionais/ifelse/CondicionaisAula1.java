package EstruturasCondicionais.ifelse;

public class CondicionaisAula1 {
    public static void main(String[] args) {
        int i = 70;
        if (i <= 60) {
            System.out.println("Você está na velocidade permitida");
        } else {
            System.out.println("Sua velocidade não é permitida");
        }
    }
}
